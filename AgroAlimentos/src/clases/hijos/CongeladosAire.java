package clases.hijos;

public class CongeladosAire extends ProductosCongelados{

	private double nitrogeno;
	private double oxigeno;
	private double dioxidoC;
	private double vapor;
	
	public CongeladosAire() {
		
	}
	
	
	public double getNitrogeno() {
		return nitrogeno;
	}
	public void setNitrogeno(double nitrogeno) {
		this.nitrogeno = nitrogeno;
	}
	public double getOxigeno() {
		return oxigeno;
	}
	public void setOxigeno(double oxigeno) {
		this.oxigeno = oxigeno;
	}
	public double getDioxidoC() {
		return dioxidoC;
	}
	public void setDioxidoC(double dioxidoC) {
		this.dioxidoC = dioxidoC;
	}
	public double getVapor() {
		return vapor;
	}
	public void setVapor(double vapor) {
		this.vapor = vapor;
	}
	
	
	
}
